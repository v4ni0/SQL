-- Ако вече съществуват, трием в правилен ред (заради FK към PRODUCT)
IF OBJECT_ID('dbo.PRINTER','U') IS NOT NULL DROP TABLE dbo.PRINTER;
IF OBJECT_ID('dbo.LAPTOP','U')  IS NOT NULL DROP TABLE dbo.LAPTOP;
IF OBJECT_ID('dbo.PC','U')      IS NOT NULL DROP TABLE dbo.PC;
IF OBJECT_ID('dbo.PRODUCT','U') IS NOT NULL DROP TABLE dbo.PRODUCT;
GO

CREATE TABLE dbo.LAPTOP(
  code   int          NOT NULL,
  model  varchar(4)   NOT NULL,
  speed  decimal(4,0) NOT NULL,
  ram    decimal(4,0) NOT NULL,
  hd     decimal(3,0) NOT NULL,
  price  float        NOT NULL,
  screen int          NOT NULL
);

CREATE TABLE dbo.PC(
  code  int          NOT NULL,
  model varchar(4)   NOT NULL,
  speed decimal(4,0) NOT NULL,
  ram   decimal(4,0) NOT NULL,
  hd    decimal(3,0) NOT NULL,
  cd    varchar(3)   NOT NULL,
  price float        NOT NULL
);

CREATE TABLE dbo.PRODUCT(
  maker char(1)     NOT NULL,
  model varchar(4)  NOT NULL,
  type  varchar(7)  NOT NULL
);

CREATE TABLE dbo.PRINTER(
  code  int         NOT NULL,
  model varchar(4)  NOT NULL,
  color char(1)     NOT NULL,
  type  varchar(6)  NOT NULL,
  price float       NOT NULL
);

ALTER TABLE dbo.LAPTOP  ADD CONSTRAINT PK_laptop  PRIMARY KEY(code);
ALTER TABLE dbo.PC      ADD CONSTRAINT PK_pc      PRIMARY KEY(code);
ALTER TABLE dbo.PRODUCT ADD CONSTRAINT PK_product PRIMARY KEY(model);
ALTER TABLE dbo.PRINTER ADD CONSTRAINT PK_printer PRIMARY KEY(code);

ALTER TABLE dbo.LAPTOP  ADD CONSTRAINT FK_laptop_product  FOREIGN KEY(model) REFERENCES dbo.PRODUCT(model);
ALTER TABLE dbo.PC      ADD CONSTRAINT FK_pc_product      FOREIGN KEY(model) REFERENCES dbo.PRODUCT(model);
ALTER TABLE dbo.PRINTER ADD CONSTRAINT FK_printer_product FOREIGN KEY(model) REFERENCES dbo.PRODUCT(model);

